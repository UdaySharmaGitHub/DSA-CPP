class Solution{
  public:
    // Function to find floor of x
    // n: size of vector
    // x: element whose floor is to find
    int findFloor(vector<long long> v, long long n, long long x){
        
        // Your code here
        long long i =0 , j = n-1;
        long long mid = i + (j-i)/2;
        int ans = -1;
        while(i<j){
            if(v[mid] == x){
                ans = mid;
                return ans ;
            }
            else if(v[mid]<x){
                i = mid+1;
                ans = i;
            }
            else{
            j = mid-1;
            }
         mid = i + (j-i)/2;
        }
        return ans;
        
    }
};


